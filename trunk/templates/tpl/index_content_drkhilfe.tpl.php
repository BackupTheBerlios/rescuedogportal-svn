				die Personalstärke angepasst) Ausstattungsgegenstände eigenständig verfügt
				bzw. ein unmittelbarer Zugriff auf vorhandene Ausstattung des BRK-Kreisverbandes
				sichergestellt ist. Eine Mehrfachnutzung der Ausstattungsgegenstände durch
				unterschiedliche Fachdienste in einem BRK-Kreisverband ist zur Kostenreduzierung
				anzustreben. Die unten aufgeführten Ausstattungsgegenstände beziehen sich
				auf die Vorhaltung einer Rettungshundestaffel mit 5 Rettungshundeteams
				einschließlich Staffelführer. <br>
