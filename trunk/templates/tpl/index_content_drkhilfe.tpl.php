			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>7. Dienstbekleidung:</b>
				Es wird grundsätzlich die Dienst-
				und Einsatzbekleidung gemäss Einsatz- und Dienstbekleidungsordnung des Deutschen
				Roten Kreuzes getragen.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>8. Kennzeichnung der Rettungshunde:</b>
				Die Rettungshunde werden wie
				folgt gekennzeichnet: Am Halsband tragen die Hunde eine Marke, die sie als Rettungshund
				des Deutschen Roten Kreuzes ausweist. Diese Marke darf nur von Rettungshunden
				getragen werden, die tatsächlich nach Ausbildung und jährlicher Prüfung einsatzfähig
				sind oder berentet sind. <br>
				Wird der Rettungshund mit angelegter Schabracke eingesetzt, soll diese
				zum Schutz des Rettungshundes ( z. B. Erschießung durch Jagdfeuer) mit
				einem deutlich sichtbaren Roten Kreuz gekennzeichnet sein.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>9. Alarmierung:</b>
				Die Alarmierung einer Rettungshundegruppe/-staffel erfolgt im BRK-Landesverband
				Bayern in der Regel durch die zuständigen Rettungsleitstellen nach Anforderung.
				<br>
				Es gibt kein Rechtsverhältnis zwischen dem Träger des Rettungsdienstes
				und den Rettungshunde-gruppen/-staffeln.<br />
				Es ist jedoch anzustreben, dass diese Sondereinheiten bei Bedarf schnell
				alarmiert und eingesetzt werden können. Die Gewährleistung der Alarmierung
				bei notwendigen Hilfeleistungen kann einen Beitrag dazu leisten, die Folgen
				eines Notfalls erheblich zu reduzieren. Im BRK-Landesverband Bayern sind
				die Erreichbarkeiten, die Zuständigkeiten und das Leistungsvermögen der
				Rettungshundegruppen/-staffeln in die Alarm- und Einsatzpläne der BRK-Kreisverbände
				aufzunehmen.<br />
				Darüber hinaus stellen die BRK-Kreisverbände
				zur Gewährleistung einer professionellen Rettung (über den für sie zuständigen
				Rettungszweckverband) die Anbindung der Rettungshundegruppe/-staffel an die Rettungsleitstelle
				sicher. Die Rettungsleitstelle muss über die Möglichkeiten der Rettungshundegruppe/-staffel
				Kenntnis haben, um diese bei entsprechenden Einsätzen selbstständig anfordern
				zu können, damit die Rettungshundegruppe/-staffel im Bedarfsfall alarmiert werden
				kann. Die Ausrückzeit der Staffel, sowie das voraussichtliche Eintreffen am Einsatzort
				sind der Rettungsleitstelle sowie der anfordernden Stelle mitzuteilen. Bei Bedarf
				sind Nachbarstaffeln baldmöglichst zu alarmieren. Die im LV erarbeitete Einsatzdokumentation
				ist bei jedem Einsatz zu führen und dem KBL vorzulegen. Die Rettungshundegruppe/-staffeln
				sind durch geeignete technische Alarmierungsmittel auszustatten, dass sie im Einzelfall
				unverzüglich alarmiert werden können. <br>
				Der Einsatzleiter der Rettungshundestaffel hat bei Annahme des Sucheinsatzes außerdem
				zu prüfen, ob ein Bedarf an zusätzlichen Helfern (Fachdienste) besteht. Der Bedarf
				ist von der jeweiligen Lage abhängig und ist zunächst aus dem Kreisverband des
				Einsatzgebietes, ggf. dem entsendenden Kreisverband, anzufordern. Darüber hinaus
				ist der Einsatzleiter der Rettungshundestaffel verpflichtet mit Einsatzbeginn
				die Kreisbereitschaftsführung/ Org L seines BRK-Kreisverbandes in Kenntnis zu
				setzen. Die in diesem Zusammenhang erforderliche Verfahrensweisen sind individuell
				in den jeweiligen BRK-Kreisverbänden zu vereinbaren. <br />
				Bei Anforderungen von Rettungshunden im Ausland gelten besondere Regeln:<br>
				Der Einsatzauftrag erfolgt über den Katastrophenschutzbeauftragten des Landesverbandes
				nach Anforderung durch das Generalsekretariat - Team 41 Einsatzleitung. Die Einsatzfähigkeit,
				sowie die Kenntnisse der Besonderheiten beim Auslandseinsatz, sind zur Einsatzbefähigung
				nachzuweisen. Hierzu sind gesonderte Kurse zu belegen. Es erfolgt zunächst eine
				Voralarmierung bei der Zeitabläufe, Flug, Finanzierung, sowie Ausrüstung abzuklären
				sind.<br />
				Diese sind dem LV und GS sofort zu melden. Es sind nach Schadenslage nur geeignete und ausgebildete Teams einzusetzen.
				Eine ERU (Emergency Response Unit) Rettungshundestaffel besteht aus 5 Rettungshundeteams
				und einem Teamleiter, nach Schadenslage sind Technik und medizinische Versorgung
				zuzuordnen. <br>
				Nach Erteilung des Einsatzbefehls durch das GS über den Landesverband erfolgt
				unverzüglich die Abwicklung der geplanten Vorgänge. Während des Einsatzes ist
				mit der Einsatzleitung des Landesverbandes unbedingt Kontakt zuhalten und Vorgehensweisen
				mitzuteilen ggf. neu zu entscheiden. Jeder Teilnehmer eines Auslandseinsatzes
				verfasst einen persönlichen Bericht. Der Teamleiter verfasst einen Einsatzbericht
				zum Einsatzgeschehen. Das Einsatzteam arbeitet in gemeinsamen Arbeitsgesprächen
				den Einsatz auf, ggf. ist Hilfe zuzuziehen.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>10. Einsatzgebiete:</b>
				Im BRK-Landsverband Bayern muss sichergestellt sein, dass flächendeckend
				so viele Rettungshundegruppen/-staffeln vorhanden sind, dass jederzeit
				eine Rettungshundegruppe/-staffel jeden Punkt innerhalb des Landesverbandes
				in maximal 3 Stunden erreichen kann. Dazu sind unter ständiger Berücksichtigung
				der Einsatzfähigkeit die erforderlichen Gebietsaufteilungen unter der
				Leitung des BRK-Landesverbandes Bayern gemeinschaftlich zu erarbeiten,
				die über Kreis- und Landesverbandsgrenzen hinausgehen.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>11. Aufstellung von Rettungshundegruppen/-staffeln:</b>
				Die Aufstellung von neuen Rettungshundegruppen/-staffeln
				in den BRK-Kreisverbänden des BRK-Landesverbandes Bayern ist unter Beachtung der
				vorgenannten Einsatzplanungen genehmigungspflichtig und kann somit erst nach Zustimmung
				durch den Landesausschuß der Bereitschaften erfolgen. Wird durch den Fachberater
				in Zusammenarbeit mit dem Landesbeauftragten für die Rettungshundearbeit ein Bedarf
				für eine weitere Rettungshundegruppe/-staffel für notwendig erachtet, sind aus
				ökonomischen Gründen und zur kurzfristigen Erlangung eines effizienten einsatzwerten
				KV-übergreifenden Modells anzustreben. Bei der Errichtung einer neuen Staffel
				empfiehlt sich eine Beratung durch den Landesverband bzw. den Fachberater.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>12. Landesbeauftragter/ Fachberater für die Rettungshundearbeit:</b>
				Die Rettungshundearbeit ist im Bewußtsein des Gesamtverbandes noch mehr
				zu verankern. Der BRK-Landesverband Bayern stellt durch die Berufung eines
				Landesbeauftragten für die Rettungshundearbeit sicher, dass dies in den
				nachgeordneten Ebenen entsprechend berücksichtigt wird. Dem Landesbeauftragten
				wird ein Fachberater aus den Staffeln zur Verfügung gestellt, um fachspezifische
				Inhalte der Rettungshundearbeit im Landesverband einzubringen und zu vertreten.
				Der Landesbeauftragte wie auch der Fachberater für die Rettungshundearbeit
				werden im BRK-Landesverband Bayern vom Landesausschuss der Bereitschaften
				ernannt. Sie sind dem Landesausschuss direkt unterstellt und sind Fachvorgesetzte
				für die Rettungshundearbeit im Landesverband.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>13. Situationsbeschreibung (IST):</b>
				Zum 01.12.1999 sind im BRK-Landesverband
				Bayern 6 Rettungshundegruppen und 5 Rettungshundestaffeln mit unterschiedlicher
				Stärke und unterschiedlichem Ausbildungsstand tätig.
				</span>
