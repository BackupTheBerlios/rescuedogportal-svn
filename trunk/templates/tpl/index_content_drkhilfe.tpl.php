				<br /><br />
				<b>Richtlinien für die Rettungshundearbeit</b>
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>1. Grundsätzliches:</b>
				Die BRK-Rettungshundearbeit ist Bestandteil des BRK-Fachdienstes Sanitätsdienst
				und ist in einer eigenen Prüfungs- und Prüferordnung definiert, welche vom DRK-Präsidium
				am 12 Juni 1996 und vom DRK-Präsidialrat am 04./05. Juli 1996 nach § 19.3 der
				DRK-Satzung verabschiedet wurde. Die DRK-Rettungshundearbeit ist damit Teil des
				rotkreuzspezifischen Wirkens und ermöglicht national und international die Verhütung
				und Linderung menschlichen Leidens durch den Einsatz ehrenamtlicher Mitarbeiter
				und Mitarbeiterinnen.<br>
				Der Rettungshundeführer mit geprüftem Rettungshund wird als Rettungshundeteam
				bezeichnet. Eine Rettungshundestaffel besteht aus mindestens 5 Rettungshundeteams.
				Eine Anzahl von 2 - 4 Rettungshundeteams versteht sich als Rettungshundegruppe.
				Die eigenständige Durchführung von Sucheinsätzen sollte nur als Rettungshundestaffel
				erfolgen. Bei Erfordernis müssen sich mehrere Rettungshundegruppen zur
				Durchführung von Sucheinsätzen unter einem Staffelführer zu einer Rettungshundestaffel
				formieren. Darüber hinaus besteht die Möglichkeit, dass sich einzelne Rettungshundegruppen
				für einen Sucheinsatz einer bestehenden Rettungshundestaffel unter deren
				Führung anschließen. <br>
				Grundsätzlich darf ein Rettungshundeeinsatz nur von ausgebildeten, einsatzfähigen
				und gemäß Prüfungs- und Prüferordnung geprüften Rettungshundeteams durchgeführt
				werden. <br>
				Die Entscheidung über die Einsatzfähigkeit trifft der diensthabende Einsatzleiter
				der Rettungshundestaffel in eigener Verantwortung. Der Rettungshund ist Eigentum
				des Rettungshundeführers. Rechtlich ist innerhalb des Deutschen Roten Kreuzes
				( z. B. bei Ausbildungen und Einsätzen) der zum Dienst eingebrachte Rettungshund
				mit jedem anderen Einsatzmittel gleichzusetzen, dass der BRK-Helfer aus seinem
				persönlichen Eigentum für die Durchführung von satzungsgemäßen Aufgaben im BRK
				zur Verfügung stellt. Der Kostenersatz und die Absicherung von Risiken und Haftungsfragen
				sind über den BRK-Kreisverband zu regeln. Gemäß der Grundsätze des Roten Kreuzes
				ist eine kooperative Mitgliedschaft in anderen rechtsfähigen Vereinigungen im
				Bereich der Rettungshundearbeit auszuschließen.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>2. Aufgaben:</b>
				Die Rettungskette beginnt mit den Sofortmaßnahmen bei den Betroffenen. Diese
				müssen doch unter Umständen erst gefunden und aus dem Gefahrenbereich gerettet
				werden, was den Einsatz ausgebildeter Rettungshundeteams erfordert. Die Hilfeleistung
				der Rettungshundestaffel im BRK-Landesverband Bayern besteht aus folgenden Maßnahmen:<br>
				I. Suchen von vermissten Personen im unwegsamen Gelände (Flächensuche).<br>
				II. Suchen von vermissten Personen, die in eingestürzten Gebäuden eingeschlossen
				oder verschüttet sind (Trümmersuche).<br>
				III. Suchen von vermissten Personen im Wasser (Wassersuche).<br>
				IV. Veranlassen der Rettung. <br>
				V. Durchführung lebensrettender Sofortmaßnahmen bei diesen Personen. <br>
				VI. Übergabe der Verletzten an den Sanitäts-/Rettungsdienst. <br>
				Zur Unterstützung der Sucharbeit bedarf es neben der Mitwirkung dieser
				Rettungshundeteams ggf. auch der Unterstützung anderer Fachdienste des
				Roten Kreuzes ( Fernmeldedienst; Technischer Dienst; Wasserwacht; Unterstützung
				in schwerem Gelände, Bildung von Suchketten) sowie anderer Hilfsorganisationen.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>3. Voraussetzungen Rettungshundeführer:</b>
				- Aktive Mitgliedschaft im Bayerischen Roten Kreuz. <br>
				- Mindestalter 18 Jahre ( zur Einsatzfähigkeit). <br>
				- Gesundheitliche Eignung (Einsatzfähigkeit für Auslandseinsätze gelten besonderer
				Regelungen).<br>
				&nbsp;&nbsp;&nbsp;Der Nachweis über die gesundheitliche Eignung obliegt dem Kreisverband.
				<br>
				- Rettungshund: Höchstalter 5 Jahre (bei Ausbildungsbeginn) <br>
				- Gesundheitszeugnis . Die Verantwortung für die gesundheitliche Eignung trägt
				der Hundeführer. <br>
				- Wesensfestigkeit, d.h. der Hund muss sich ohne Einfluß des Hundeführers anderen
				Personen und Hunden gegenüber neutral und sozialverträglich verhalten.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>4. Aus-, Fort- und Weiterbildung:</b>
				Damit im Einzelfall die Sucharbeit von Rettungshundeteams professionell durchgeführt
				werden kann, bedarf es einer ausreichenden internen und externen Aus-, Fort- und
				Weiterbildung der Rettungshundeführer und Hunde. Für deren Nachweis trägt die
				Kreisbereitschaft des BRK-Kreisverbandes die Verantwortung. Die Rettungshundeführer
				müssen folgende Grundqualifikationen und Kenntnisse nachweisen: <br>
				Erste-Hilfe-Ausbildung <br>
				Einführungsseminar <br>
				Sanitätsausbildung (mindestens SAN B) <br>
				Hundehaltung / Kynologie <br>
				Trümmerkunde <br>
				Organisation und Einsatztaktik beim Rettungshundeeinsatz<br>
				Unfallverhütung/Sicherheit im Einsatz <br>
				Verhaltensgrundsätze bei Transporten <br>
				Die Ausbildung der Rettungshundeteams erfolgt gemäß der Prüfungs- und Prüferordnung
				und den bestehenden Ausbildungsrichtlinien, überregional auf der Ebene des BRK-Landesverbandes
				Bayern, regional in den BRK-Kreisverbänden bzw. in den jeweiligen Rettungshundegruppen/-staffeln
				durch Ausbilder der Rettungshundearbeit. Ausbilder in der Rettungshundearbeit
				bedürfen einer besonderen Qualifikation gemäß der gültigen Richtlinien und Bestimmungen
				des DRK-Bundesverbandes und des BRK-Landesverbandes Bayern (bestimmt der KBL).
				Die Koordination, Thematik und Terminierung der Aus- u. Fortbildung erfolgt über
				den Landesausbildungsbeauftragten oder dessen Vertreter.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>5. Prüfungen:</b>
				Auf der Grundlage der Prüfungs- und Prüferordnung für die Rettungshundearbeit
				im Deutschen Roten Kreuz, werden im BRK-Landesverband Bayern jährlich
				zentrale Rettungshundeprüfungen für alle Rettungshundeteams durchgeführt.
				Somit ist eine kontinuierliche und gezielte Prüfungsvorbereitung für alle
				Rettungshundeführer gewährleistet. Die Anmeldung zur Prüfung erfolgt durch
				die Staffelleitung mit deren Zustimmung.
				</span>
			</p>
			<p style="alignment:justify; text-align:justify">
				<span class="normalfont">
				<b>6. Materialausstattung:</b>
